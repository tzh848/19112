package com.st.bean;

public class Visit {
	private String visit_id;
	private String visit_customer_name;
	private String visit_user_name;
	private String visit_time;
	private String visit_time_text;
	private String visit_info;
	private String visit_pattern;
	private int visit_pay;
	private double visit_money;
	
	public String getVisit_time_text() {
		return visit_time_text;
	}
	public void setVisit_time_text(String visit_time_text) {
		this.visit_time_text = visit_time_text;
		this.visit_time = visit_time;
	}
	public String getVisit_id() {
		return visit_id;
	}
	public void setVisit_id(String visit_id) {
		this.visit_id = visit_id;
	}
	public String getVisit_customer_name() {
		return visit_customer_name;
	}
	public void setVisit_customer_name(String visit_customer_name) {
		this.visit_customer_name = visit_customer_name;
	}
	public String getVisit_user_name() {
		return visit_user_name;
	}
	public void setVisit_user_name(String visit_user_name) {
		this.visit_user_name = visit_user_name;
	}
	public String getVisit_time() {
		return visit_time;
	}
	public void setVisit_time(String visit_time) {
		this.visit_time = visit_time;
	}
	public String getVisit_info() {
		return visit_info;
	}
	public void setVisit_info(String visit_info) {
		this.visit_info = visit_info;
	}
	public String getVisit_pattern() {
		return visit_pattern;
	}
	public void setVisit_pattern(String visit_pattern) {
		this.visit_pattern = visit_pattern;
	}
	public int getVisit_pay() {
		return visit_pay;
	}
	public void setVisit_pay(int visit_pay) {
		this.visit_pay = visit_pay;
	}
	public double getVisit_money() {
		return visit_money;
	}
	public void setVisit_money(double visit_money) {
		this.visit_money = visit_money;
	}
	
	
}
